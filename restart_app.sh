#!/bin/bash

# Скрипт для полного перезапуска приложения

echo "Останавливаем все процессы..."

# Находим и останавливаем все процессы Python
pkill -f "python"

# Находим и останавливаем все процессы Node.js
pkill -f "node"

# Ждем, пока все процессы остановятся
sleep 2

echo "Очищаем локальное хранилище..."
cd frontend/chrome-extension
./clear-storage.sh
cd ../..

echo "Запускаем приложение заново..."
./start_app.sh

echo "Приложение перезапущено!"
